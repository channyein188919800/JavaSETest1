package day3.operators;

public class Relational_OP {
    public static void main(String[] args) {
        int num1=23;
        int num2=14;
        System.out.println(num1<num2);
        System.out.println(num1>num2);
        System.out.println(num1<=num2);
        System.out.println(num1>=num2);
        System.out.println(num1!=num2);
        System.out.println(num1==num2);
    }
}
