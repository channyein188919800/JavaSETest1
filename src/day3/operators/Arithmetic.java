package day3.operators;

public class Arithmetic {
    public static void main(String[] args) {
        //Arrithmatic operator
        int num1=1;
        int num2=2;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
}
